package com.dim0va;

public class NewElement {
    private boolean isGreen;
    private int element;

    public NewElement(boolean isGreen, int element) {
        this.isGreen = isGreen;
        this.element = element;
    }

    public boolean getGreen() {
        return isGreen;
    }

    public int getElement() {
        return element;
    }
}
